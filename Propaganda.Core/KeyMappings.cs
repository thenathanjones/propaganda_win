using System.Windows.Input;

namespace Propaganda.Core
{
    public abstract class KeyMappings
    {
        public const Key BACK = Key.Escape;
        public const Key ENTER = Key.Enter;
        public const Key NEXT = Key.F8;

        public const Key PAUSE = Key.Space;
        public const Key PLAY = Key.P;
        public const Key PREVIOUS = Key.F7;
        public const Key STOP = Key.B;
    }
}